var app=new Vue({
    el:"#app",
    data:function(){
        return {
            formData:{
                student_id:null,
                student_name:null,
                page:1,
            },
            pager:null,
            list:null
        }
    },
    methods:{
        reset(){
            this.formData.student_id=null;
            this.formData.student_name=null;
            this.research();
        },
        research(i){
            this.formData.page=i||1;
            this.getList();
        },
        getList(){
            getData('/admin/student/list',this,{formData:this.formData}).then(r=>{
                this.list=r.list;
                this.pager=r.pager;
            })
        }
    },
    created(){
        this.getList()
    },
    mounted(){
    }
})